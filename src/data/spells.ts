export function getSpell(id: string): SpellDefinition | undefined {
  return SPELLS.find(s => s.id === id);
}

export function getSpellsByClass(spellClass: 'wizard' | 'priest'): SpellDefinition[] {
  return SPELLS.filter(s => s.class === spellClass);
}

export function getSpellsByTier(tier: number): SpellDefinition[] {
  return SPELLS.filter(s => s.tier === tier);
}

export function getSpellsByClassAndTier(spellClass: 'wizard' | 'priest', tier: number): SpellDefinition[] {
  return SPELLS.filter(s => s.class === spellClass && s.tier === tier);
}
